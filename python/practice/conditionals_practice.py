color = input('Enter Color Here: ')

if color == 'R' or color == 'r':
    print(f'{color} is Red')
elif color == 'B' or color == 'b':
    print(f'{color} is Blue')
elif color == 'G' or color == 'g':
    print(f'{color} is Green')
elif color == 'Y' or color == 'y':
    print(f'{color} is Yellow')
else:
    print(f'{color} is not in the Database')